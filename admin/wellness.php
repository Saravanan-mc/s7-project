        <h1>Your Guide to Holistic Wellness</h1>
        <p>Achieving wellness involves balancing various aspects of your life. Explore the sections below to learn more.</p>

        <h2 id="mental">Mental Health</h2>
        <p>Mental well-being is as important as physical health. Practicing mindfulness, managing stress, and seeking support when needed are key components.</p>
        <h3>Tips for Mental Well-being:</h3>
        <ul>
            <li>Meditation and deep breathing exercises.</li>
            <li>Journaling thoughts and feelings.</li>
            <li>Limiting screen time and social media.</li>
            <li>Connecting with friends and family.</li>
        </ul>

        <h2 id="physical">Physical Fitness</h2>
        <p>Regular physical activity is vital for a healthy body and mind. Find an activity you enjoy and make it a consistent part of your routine.</p>
        <h3>Exercise Recommendations:</h3>
        <ul>
            <li>At least 150 minutes of moderate-intensity aerobic activity per week.</li>
            <li>Strength training exercises twice a week.</li>
            <li>Flexibility and balance exercises.</li>
        </ul>

        <h2 id="nutrition">Nutrition</h2>
        <p>Fueling your body with nutritious food provides the energy and building blocks it needs to function optimally.</p>
        <h3>Healthy Eating Habits:</h3>
        <ul>
            <li>Eat a variety of fruits and vegetables.</li>
            <li>Choose whole grains over refined grains.</li>
            <li>Include lean proteins and healthy fats.</li>
            <li>Stay hydrated by drinking plenty of water.</li>
        </ul>

        <h2 id="sleep">Sleep Hygiene</h2>
        <p>Adequate and quality sleep is fundamental for physical and mental recovery. Improve your sleep habits for better overall health.</p>
        <h3>Improving Sleep:</h3>
        <ul>
            <li>Maintain a consistent sleep schedule.</li>
            <li>Create a relaxing bedtime routine.</li>
            <li>Ensure your sleep environment is dark, quiet, and cool.</li>
            <li>Avoid caffeine and heavy meals before bed.</li>
        </ul>
